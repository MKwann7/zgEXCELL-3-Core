<?php

namespace Entities\Directories\Components\Vue;

class MyDirectoryMainApp extends VueApp
{
    protected string $appNamePlural = "My Directories";
    protected string $appNameSingular = "My Directory";

    public function __construct($domId, ?VueModal &$modal = null)
    {
        $this->enableSlickSortContainerMixin();
        $this->enableSlickSortElementMixin();
        $this->enableSlickSortHandleDirective();

        $this->setDefaultComponentId(ListDirectoryWidget::getStaticId())->setDefaultComponentAction("view");

        parent::__construct($domId, $modal);
    }

    public function renderAppData() : string
    {
        return "
        showNewSelection: true,
        ";
    }
}